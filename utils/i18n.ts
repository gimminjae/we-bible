type MsgTree = Record<string, unknown>

function getValue(obj: MsgTree, path: string): string {
  const seg = path.split(".")
  let cur: unknown = obj
  for (const key of seg) {
    if (!cur || typeof cur !== "object" || !(key in cur)) return path
    cur = (cur as Record<string, unknown>)[key]
  }
  return typeof cur === "string" ? cur : path
}

export function translate(lang: AppLanguage, key: string, vars?: Record<string, string | number>): string {
  let text = getValue(messages[lang], key)
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replaceAll(`{${k}}`, String(v))
    }
  }
  return text
}

export function useI18n() {
  const { language } = useAppSettings()
  return {
    t: (key: string, vars?: Record<string, string | number>) => translate(language, key, vars),
    language,
  }
}
